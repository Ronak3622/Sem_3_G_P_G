public class AAC_Indentifiers {
    public static void main (String []args){
        int age = 19;// Good
        int a = 19;// ok, but not so easy to understand that a actually is 
        System.out.println(age);
        System.out.println(a);
        /*
         * All java variable must be identified with unique names.
         * 
         * These unique names are called identifiers.
         * 
         * Identifiers can be short names (like x and y) or more descriptive names (age,sum,totalvolume).
         * 
         * Note : It is recommended to use descriptive names in order to create understable and maintable
         * 
         * Rules:
         * 1. Names can conatain letters, digits, underscores, and dollar signs
         * 2. Names must begin with a letter.
         * 3. Names should start with a lowercase letter, and cannot contain whitesspace
         * 4. Names can also begin with $ and _(but we will not us eit in this tutorial)
         * 5. Names are case-sensitive
         * 6. Reserved words cannot be used as names
         */
    }
}
